/**
 * A power consumer that uses a dynamic amount of power.
 */
public class GasDynamicConsumePower extends ConsumePower {

    private final Floatf<Building> usage;

    public GasDynamicConsumePower(Floatf<Building> usage) {
        super(0, 0, false);
        this.usage = usage;
    }

    @Override
    public float requestedPower(Building entity) {
        return usage.get(entity);
    }
}
